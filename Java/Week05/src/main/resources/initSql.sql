CREATE TABLE student
(Sno    varchar(20)    NOT NULL,
 Sname  varchar(20)    NOT NULL,
 sex   varchar(20)    NOT NULL,
 age   INT            NOT NULL,
 dept  varchar(20)    NOT NULL,
 PRIMARY KEY (Sno)
);

CREATE TABLE course
(Cno    varchar(20)    NOT NULL,
 Cname  varchar(20)    NOT NULL,
 hours VARCHAR(20)    NOT NULL,
 PRIMARY KEY (Cno)
);

CREATE TABLE SC
(Sno    varchar(20)    NOT NULL,
 Cno    varchar(20)    NOT NULL,
 grade  INT    ,
 PRIMARY KEY (Sno,Cno)
);

